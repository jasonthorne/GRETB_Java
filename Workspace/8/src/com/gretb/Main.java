package com.gretb;

public class Main {

	public static void main(String[] args) {
		
		//loops
		
		//all loops needs some sort of counter to keep track of the current iteration of the loop.
		
		int counter=0;
		
		while(counter<5) {
			counter++;
			System.out.println("counter is: " + counter);
			
		}
		
		int i; 
		//incrementors:
		//i+=num; //same as 'i=i+num' 
		
		
		i=0;
		while(i<10) {
			i++;
			System.out.println(i);
		}
		
	
	}

}
