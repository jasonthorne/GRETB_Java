package com.gretb;

public class Maths {
	
	public static void add(int num1, int num2) {
		int sum = num1 + num2;
		System.out.println("addition sum: " + sum);
	}
	
	public static void multiply(int num1, int num2) {
		int sum = num1 * num2;
		System.out.println("multiplication sum: " + sum);
	}
	
}
