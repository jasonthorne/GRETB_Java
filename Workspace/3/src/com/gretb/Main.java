package com.gretb;


/**
 * If statements give us a choice to do something if some condition arrises
 * 
 */

public class Main {

	public static void main(String[] args) {
		
		//defining an int called age:
		int age=21;
		
		System.out.println(age>18); //true
		System.out.println(age>18); //false
		
		if (age<18) System.out.println("No admittance");
		else System.out.println("Admittence");
		
		
		String name = "Bob";
		//'name' is a reference (an address in memory), 'Bob' is the value //+++++++++++++++++++++++++++
		
		System.out.println(name);


	}

}
